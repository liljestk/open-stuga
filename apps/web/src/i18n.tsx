interface I18nValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, values?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: Readonly<{ children: ReactNode }>) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const stored = localStorage.getItem("climate-twin-locale");
    if (stored === "fi" || stored === "en") return stored;
    return navigator.language.toLowerCase().startsWith("fi") ? "fi" : "en";
  });

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const setLocale = useCallback((next: Locale) => {
    if (next === "fi") {
      localStorage.setItem("climate-twin-locale", "fi");
    } else {
      localStorage.setItem("climate-twin-locale", "en");
    }
    document.documentElement.lang = next;
    setLocaleState(next);
  }, []);

  const t = useCallback((key: TranslationKey, values: Record<string, string | number> = {}) => {
    const dictionary = locale === "fi" ? fi : en;
    const template = (dictionary as Record<string, string>)[key] ?? (en as Record<string, string>)[key] ?? key;
    return Object.entries(values).reduce(
      (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
      template,
    );
  }, [locale]);

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used within I18nProvider");
  return context;
}
